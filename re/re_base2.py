digit2='[一二三四五六七八九两十]+[十百千万零]*[一二三四五六七八九两半]?'   #存在不能匹配的情况，例如五千三百一十二
p=re.compile('(?=.{1,}$)'+digit)
s='五十二，一千零一，五百万，五千三百一十二,一'
